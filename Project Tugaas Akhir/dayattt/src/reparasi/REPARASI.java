    public void caripelanggan() {

        DefaultTableModel tabelnyo = new DefaultTableModel();
        tabelnyo.addColumn("ID PELANGGAN");
        tabelnyo.addColumn("NAMA");
        tabelnyo.addColumn("NOHP");

        try {
            koneksi connection = new koneksi();
            Statement stat = connection.configDB().createStatement();
            String sql = "SELECT * FROM pelanggan where ID_PELANGGAN like '%" + CARIPELANGGAN.getText() + "%' OR nama like '%" + CARIPELANGGAN.getText() + "%' OR nohp like '%" + CARIPELANGGAN.getText() + "%'";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                tabelnyo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),});
            }
            jTable2.setModel(tabelnyo);

        } catch (SQLException e) {
        }

    }
