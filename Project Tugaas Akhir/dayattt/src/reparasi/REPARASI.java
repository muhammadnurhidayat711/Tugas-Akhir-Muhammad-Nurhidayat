    public void getData() {

        mdl.getDataVector().removeAllElements();
        mdl.fireTableDataChanged();
        try {
//membuat statemen untuk memanggil data table tabel_buku
            koneksi connection = new koneksi();
            Statement stat = connection.configDB().createStatement();
            String sql = "Select * from pelanggan";
            ResultSet res = stat.executeQuery(sql);
//pengecekan terhadap data tabel_buku
            while (res.next()) {
                Object[] obj = new Object[3];
//                obj[0] = res.getString("ID_PEMBELIAN");
//                obj[1] = res.getString("ID_PELANGGAN");
//                obj[2] = res.getString("ID_BARANG");
                obj[0] = res.getString("ID_PELANGGAN");
                obj[1] = res.getString("NAMA");
                obj[2] = res.getString("NOHP");
                mdl.addRow(obj);
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }

    }

