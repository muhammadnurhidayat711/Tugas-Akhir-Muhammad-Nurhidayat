    private void Simpan() {

        Date d = (Date) jDateChooser1.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String waktu = sdf.format(d).toString();
        String kode = KODETRANSAKSI.getText();
        String nama = NAMA.getText();
        String total = TOTALHARGA.getText();
        String bayar = BAYARAN.getText();
        String kembalian = KEMBALIAN.getText();
        String no = ("0");
        String usr = USER.getText();

        String queryTambah = "INSERT INTO transaksibarangkeluar(NO, KODE, NAMA, TOTAL, BAYAR, KEMBALIAN, WAKTU, USER) VALUES (?,?,?,?,?,?,?,?)";
        try {
            pst = conn.prepareStatement(queryTambah);
            pst.setString(1, no);
            pst.setString(2, kode);
            pst.setString(3, nama);
            pst.setString(4, total);
            pst.setString(5, bayar);
            pst.setString(6, kembalian);
            pst.setString(7, waktu);
            pst.setString(8, usr);
            if (kode.isEmpty()|| nama.isEmpty() || bayar.isEmpty() || total.isEmpty()||kembalian.isEmpty()||waktu.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Form bayar masih kosong");
            } else {
                if (JOptionPane.showConfirmDialog(null, "Apakah Data Yang Diisi Sudah Benar?", "Tambah Data", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Tersimpan !");
                KODETRANSAKSI.setText(null);
                NAMA.setText(null);
                TOTALHARGA.setText(null);
                BAYARAN.setText(null);
                KEMBALIAN.setText(null);
                jDateChooser1.setDate(null);
               dataTable();
            }}
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "MAAF DATA TELAH TERSEDIA");
        }
    }
