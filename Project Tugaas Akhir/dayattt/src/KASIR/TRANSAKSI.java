 public void caripelanggan() {

        DefaultTableModel tabelnyo = new DefaultTableModel();
        tabelnyo.addColumn("NO");
        tabelnyo.addColumn("KODE");
        tabelnyo.addColumn("NAMA");
        tabelnyo.addColumn("TOTAL HARGA");
        tabelnyo.addColumn("BAYAR");
        tabelnyo.addColumn("KEMBALIAN");
        tabelnyo.addColumn("WAKTU");
        tabelnyo.addColumn("USER");
        

        try {
            koneksi connection = new koneksi();
            Statement stat = connection.configDB().createStatement();
            String sql = "SELECT * FROM transaksibarangkeluar where KODE like '%" + CARI.getText() + "%' OR NAMA like '%" + CARI.getText() +"%'";
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()) {
                tabelnyo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getDate(7),
                    rs.getString(8),
                
                });
            }
            jTable1.setModel(tabelnyo);

        } catch (SQLException e) {
        }

    }
    
    public void getDataPembelian() {

        try {
//membuat statemen untuk memanggil data table tabel_buku
            koneksi connection = new koneksi();
            Statement stat = connection.configDB().createStatement();
            String sql = "SELECT masukmemasuk.ID_PELANGGAN, pelanggan.NAMA, "
                    + "SUM(masukmemasuk.HARGA) AS TOTAL FROM masukmemasuk "
                    + "LEFT JOIN pelanggan ON masukmemasuk.ID_PELANGGAN=pelanggan.ID_PELANGGAN "
                    + "WHERE (masukmemasuk.CODE ='"+KODETRANSAKSI.getText()+"' AND masukmemasuk.STATUS= 'SELESAI')";
            ResultSet rs = stat.executeQuery(sql);
//pengecekan terhadap data tabel_buku
             while(rs.next())
                {
                    NAMA.setText(rs.getString("NAMA"));
                    TOTALHARGA.setText(rs.getString("TOTAL"));
                   
                }
            }catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"GAGAL");
            }
        
    }

 private void dataTable(){
        try {
            String bacaSemuaData = "SELECT NO as 'No', KODE as 'KODE', NAMA as 'NAMA', TOTAL as 'TOTAL', BAYAR as 'BAYAR', KEMBALIAN as 'KEMBALIAN', WAKTU as 'WAKTU', USER as 'USER' FROM transaksibarangkeluar";
            pst = conn.prepareStatement(bacaSemuaData);
            res = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(res));
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
 
 
